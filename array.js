
let testArray=[1,2,3,4,5,6,7,8,9];
console.log("This is original array:",testArray);


testArray.push(10);
console.log("Push/Add new data to the last index of array:",testArray);


testArray.pop();
console.log("Pop/delete data from last index of array:",testArray);


testArray.shift();
console.log("Shift/delete data from starting index of array:",testArray);


testArray.unshift(0);
